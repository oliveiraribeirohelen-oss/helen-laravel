    <h1>Receita do Brownie Perfeito</h1>

    <h2>Ingredientes</h2>
    <ul>
        <li>200g de chocolate meio amargo</li>
        <li>150g de manteiga</li>
        <li>1 xícara e meia de açúcar</li>
        <li>3 ovos</li>
        <li>1 xícara de farinha de trigo</li>
        <li>1 xícara e meia de Nescau</li>
        <li>1 pitada de sal</li>
        <li>1/2 xícara de nozes (opcional)</li>
    </ul>

    <h2>Modo de Preparo</h2>
    <ol>
        <li>Preaqueça o forno a 180°C.</li>
        <li>Derreta o chocolate e a manteiga em banho-maria ou no micro-ondas.</li>
        <li>Em uma tigela, bata os ovos com o açúcar até formar uma mistura fofa e clara.</li>
        <li>Adicione o chocolate derretido à mistura de ovos e mexa bem.</li>
        <li>Acrescente a farinha, o Nescau e a pitada de sal. Misture até ficar homogêneo.</li>
        <li>Se desejar, adicione as nozes picadas.</li>
        <li>Despeje a massa em uma forma untada e enfarinhada.</li>
        <li>Leve ao forno por cerca de 25 a 30 minutos, ou até que ao espetar um palito, ele saia com algumas migalhas úmidas.</li>
        <li>Deixe esfriar, corte em pedaços e sirva!</li>
    </ol>
